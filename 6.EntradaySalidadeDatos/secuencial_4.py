"""
Programa que solicite dos numros y posteriormente los muestra la suma de ambos valroes en pantalla

"""


print("Por favor ingresa dos datos para realizar la suma")

num1 = int(input("INgresa el primer valor: "))
num2 = int(input("INgresa el segundo valor: ")) 

dato = num1 + num2
print(f"La suma de ambos numeros es {dato}")